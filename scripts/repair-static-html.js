let touchedFiles = 0;

for (const fileName of allHtmlFiles) {
  const filePath = path.join(publicDir, fileName);
  let content = fs.readFileSync(filePath, "utf8");
  const original = content;

  for (const [pattern, replacement] of globalTextReplacements) {
    content = content.replace(pattern, replacement);
  }

  for (const [pattern, replacement] of broadIconReplacements) {
    content = content.replace(pattern, replacement);
  }

  const fileSpecific = perFileReplacements[fileName] ?? [];
  for (const [pattern, replacement] of fileSpecific) {
    content = content.replace(pattern, replacement);
  }

  if (content !== original) {
    fs.writeFileSync(filePath, content, "utf8");
    touchedFiles += 1;
  }
}

console.log(`Updated ${touchedFiles} HTML files.`);
